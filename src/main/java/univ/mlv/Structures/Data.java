package univ.mlv.Structures;

/**
 *
 * @author fadhela-pc
 */
public class Data {
    public static String user;
    public static String source; 
    public static String text;
    public static String author;

    /**
     * @return the user
     */
    public static String getUser() {
        return user;
    }

    /**
     * @param aUser the user to set
     */
    public static void setUser(String aUser) {
        user = aUser;
    }

    /**
     * @return the source
     */
    public static String getSource() {
        return source;
    }

    /**
     * @param aSource the source to set
     */
    public static void setSource(String aSource) {
        source = aSource;
    }

    /**
     * @return the text
     */
    public static String getText() {
        return text;
    }

    /**
     * @param aText the text to set
     */
    public static void setText(String aText) {
        text = aText;
    }

    /**
     * @return the author
     */
    public static String getAuthor() {
        return author;
    }

    /**
     * @param aAuthor the author to set
     */
    public static void setAuthor(String aAuthor) {
        author = aAuthor;
    }

    
}
